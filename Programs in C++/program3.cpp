#include <iostream>
using namespace std;

int main()
{
    float a, b;

    cout << "Enter the value of a and b: " << endl;
    cin >> a >> b;

    cout << "The sum of a and b is " << a + b << endl;
    cout << "The difference of a and b is " << a - b << endl;
    cout << "The product of a and b is " << a * b << endl;
    cout << "The quotient of a and b is " << a / b << endl;

    return 0;
}